export function getExercisesByGroup(group: string): LibraryExercise[] {
  return EXERCISE_LIBRARY.filter((e) => e.muscleGroup === group);
}

export function searchExercises(query: string): LibraryExercise[] {
  const q = query.toLowerCase().trim();
  if (!q) return EXERCISE_LIBRARY;
  return EXERCISE_LIBRARY.filter(
    (e) =>
      e.name.toLowerCase().includes(q) ||
      e.muscleGroup.toLowerCase().includes(q) ||
      e.equipment.toLowerCase().includes(q) ||
      e.secondaryMuscles.some((m) => m.includes(q))
  );
}

export function getDifficultyLabel(d: Difficulty): string {
  const map: Record<Difficulty, string> = {
    iniciante: "Iniciante",
    intermediario: "Intermediário",
    avancado: "Avançado",
  };
  return map[d];
}

export function getDifficultyColor(d: Difficulty): string {
  const map: Record<Difficulty, string> = {
    iniciante: "#22c55e",
    intermediario: "#f59e0b",
    avancado: "#ef4444",
  };
  return map[d];
}
